# === utilitários ===
def draw_level(surface, level, camera=(0,0)):
    cam_x, cam_y = camera
    for row_i, row in enumerate(level):
        for col_i, ch in enumerate(row):
            x = col_i * TILE - cam_x
            y = row_i * TILE - cam_y
            if ch == 1:
                pygame.draw.rect(surface, TILE_COLOR, (x, y, TILE, TILE))      
            elif ch == 2:
                pygame.draw.rect(surface, TILE_COLOR2, (x, y, TILE, TILE))
            else:
                pygame.draw.rect(surface, BG_COLOR, (x, y, TILE, TILE))
            # se quiser grade (opcional), descomente a próxima linha
            # pygame.draw.rect(surface, GRID_COLOR, (x, y, TILE, TILE), 1)

def run(screen):
    clock = pygame.time.Clock()
    sw, sh = screen.get_size()

    # câmera (pode mover com setas/WASD)
    cam_x, cam_y = 0, 0
    cam_speed = 600  # px/s

    running = True
    while running:
        dt = clock.tick(FPS) / 1000.0

        for e in pygame.event.get():
            if e.type == pygame.QUIT:
                pygame.quit()
                sys.exit()
            if e.type == pygame.KEYDOWN:
                if e.key == pygame.K_ESCAPE:
                    running = False

        # inputs de câmera
        keys = pygame.key.get_pressed()
        move_x = 0
        move_y = 0
        if keys[pygame.K_LEFT] or keys[pygame.K_a]:
            move_x = -1
        if keys[pygame.K_RIGHT] or keys[pygame.K_d]:
            move_x = 1
        if keys[pygame.K_UP] or keys[pygame.K_w]:
            move_y = -1
        if keys[pygame.K_DOWN] or keys[pygame.K_s]:
            move_y = 1

        cam_x += int(move_x * cam_speed * dt)
        cam_y += int(move_y * cam_speed * dt)

        # limitar câmera (para não 'ver' área negativa ou muito além do mapa)
        max_x = max(0, COLS * TILE - sw)
        max_y = max(0, ROWS * TILE - sh)
        cam_x = max(0, min(cam_x, max_x))
        cam_y = max(0, min(cam_y, max_y))

        # desenho
        screen.fill(BG_COLOR)
        draw_level(screen, LEVEL, (cam_x, cam_y))
        pygame.display.flip()

    # retorna ao chamador (ex.: menu)
    return
        

